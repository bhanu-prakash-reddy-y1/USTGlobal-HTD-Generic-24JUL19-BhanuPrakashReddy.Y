class RelationalOperators 
{
	public static void main(String[] args) 
	{
		int i = 10;
		int j = 20;
		boolean b = i == j;// equal to relational operator
		System.out.println(" == "+b);
		b = i != j;//not equal to relational operator
		System.out.println(" != "+b);
		b = i>j;//greater than  relational operator
		System.out.println(" > "+b);
		b = i<j;//lessthan relational operator
		System.out.println(" < "+b);
		b = i>=j;//grreaterthan or equal to relational operator
		System.out.println(" >= "+b);
		b = i<=j;//lessthan or equal to  relational operator
		System.out.println(" <= "+b);
		char d = 'd';
		int t = 12;
		b= d == t;//char and int comparision
		System.out.println(b);
		char a = 'A';
		int k = 65;
		b = a == k;
		System.out.println(" == "+b);
	}
}
